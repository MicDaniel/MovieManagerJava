    private void createButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createButtonActionPerformed
       
        if(checkData()){
            Connection connection = DB_CONNECTION.getConnection();
            PreparedStatement ps;

            try {
                ps = connection.prepareStatement("INSERT INTO `users`(`first_name`, `last_name`, `username`, `password`) VALUES (?,?,?,?)");
                ps.setString(1, firstNameTField.getText());
                ps.setString(2, lastNameTField.getText());
                ps.setString(3, usernameTField.getText());
                ps.setString(4, passwordField.getPassword().toString());
                
                if(ps.executeUpdate() != 0){
                    JOptionPane.showMessageDialog(null,"Account Created");
                    LOGIN_WINDOW loginWindow = new LOGIN_WINDOW(); 
                    loginWindow.setVisible(true);
                    loginWindow.pack();
                    loginWindow.setLocationRelativeTo(null);
                    loginWindow.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                    this.dispose();
                    
                }
                else {
                    JOptionPane.showMessageDialog(null,"Could not create account");
                }
            } catch (SQLException ex) {
                Logger.getLogger(REGISTER_WINDOW.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_createButtonActionPerformed

