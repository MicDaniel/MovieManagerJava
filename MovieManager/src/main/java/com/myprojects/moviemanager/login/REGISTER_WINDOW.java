    public boolean checkData(){
        if(firstNameTField.getText().equals("") || lastNameTField.getText().equals("") ||
                usernameTField.getText().equals("") || usernameTField.getText().toLowerCase().equals("Username") ||
                passwordField.getPassword().toString().equals("") || passwordField.getPassword().toString().toLowerCase().equals("Password")) {
             JOptionPane.showMessageDialog(null, "Fill all the fields!");
            return false;
        }
        else if(isUsernameAvailable(usernameTField.getText())) {
            JOptionPane.showMessageDialog(null, "Username already exists");
            return false;
        }
        else if(!String.valueOf(passwordField.getPassword()).equals(String.valueOf(passwordcheckField.getPassword()))) {
            JOptionPane.showMessageDialog(null, "Passwords must match");
            return false;
        }
        return true;
    }
    
    public boolean isUsernameAvailable(String username) {
        
        boolean usernameExist = false;
        Connection connection = DB_CONNECTION.getConnection();
        PreparedStatement ps;
        ResultSet rs;
        
        try {
            ps = connection.prepareStatement("SELECT * FROM `users` WHERE `username` = ?");
            ps.setString(1, usernameTField.getText());
            
            rs = ps.executeQuery();
            
            if(rs.next()) {
                usernameExist = true;
            }
        } catch (SQLException ex) {
            Logger.getLogger(LOGIN_WINDOW.class.getName()).log(Level.SEVERE, null, ex);
        }
        
       return usernameExist; 
    }
    
